/*
    Function Anatomy
    1) A function keyword, followed by a name, followed by parentheses ().
    2) A parameter name inside the parentheses parameter1,parameter2... separated by (,).
    3) A set of code in the currly braces {}.
    4) Function arguments are values received by the function invocation.
    5) return statement return value from the function.

    Example
    function print(value){
        console.log(value); // 5
    }
    print(5);

    Anonymous Function
    Function without the name.

    Example
    let print = function(value){
        console.log(value); // 5
    };
    print(5);

    Assigning Function to a Variable
    Assign function expression to variable.

    Example
    let print = function(value){
        console.log(value); // 5
    };
    print(5);
*/

